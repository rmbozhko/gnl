#include "libft.h"

char		*ft_strcat(char *dest, const char *src)
{
	int i;
	int k;

	k = 0;
	i = 0;
	while (dest[i] != '\0')
	{
		i++;
	}
	while (src[k] != '\0')
	{
		dest[i] = src[k];
		i++;
		k++;
	}
	dest[i] = '\0';
	return (dest);
}
